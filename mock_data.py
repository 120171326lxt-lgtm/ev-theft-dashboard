def loss_curve() -> pd.DataFrame:
    epochs = np.arange(0, 201, 5)
    train = 0.55 - 0.35 * (1 - np.exp(-epochs / 60)) + np.random.normal(0, 0.003, len(epochs))
    val = 0.53 - 0.32 * (1 - np.exp(-epochs / 55)) + np.random.normal(0, 0.004, len(epochs))
    return pd.DataFrame({"epoch": epochs, "训练": train.clip(min=0.19), "验证": val.clip(min=0.20)})


